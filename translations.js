let currentLang = localStorage.getItem('theme-lang') || 'en';

function toggleLanguage() {
  currentLang = currentLang === 'en' ? 'id' : 'en';
  localStorage.setItem('theme-lang', currentLang);
  applyTranslations();
  updateLangButton();
}

function updateLangButton() {
  const btn = document.getElementById('lang-toggle');
  if(btn) {
    if(currentLang === 'en') {
      btn.innerHTML = '<img src="https://flagcdn.com/24x18/id.png" alt="ID" style="border-radius:2px; display:block;" />';
      btn.setAttribute('title', 'Switch to Indonesian');
    } else {
      btn.innerHTML = '<img src="https://flagcdn.com/24x18/gb.png" alt="EN" style="border-radius:2px; display:block;" />';
      btn.setAttribute('title', 'Switch to English');
    }
  }
}

function applyTranslations() {
  const t = translations[currentLang];
  
  // Update all elements with data-i18n attribute
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (t[key]) {
      // If element contains HTML (like <em>), use innerHTML
      if (t[key].includes('<') && t[key].includes('>')) {
        el.innerHTML = t[key];
      } else {
        // Special case for hero text and placeholders inside divs that have mixed elements
        if(el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
           // Ignore inputs except maybe placeholders which we didn't add yet
        } else {
           // Fast replacement while keeping nested SVGs intact if needed (mostly text nodes)
           // For simple text, textContent is safer, but innerHTML allows <em> elements.
           // However sometimes a button has an SVG and text. We should use span for text inside buttons.
           el.innerHTML = t[key];
        }
      }
    }
  });
}

// Ensure it runs on load
document.addEventListener('DOMContentLoaded', () => {
  updateLangButton();
  if(currentLang !== 'en') {
    applyTranslations();
  }
});
